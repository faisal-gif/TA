    Jurusan Teknologi Informasi - Politeknik Negeri Malang (POLINEMA) ikut serta berperan 
    bersama komunitas  intelektual yang amanah serta terampil  memanfaatkan ilmu pengetahuan dan teknologi guna mewujudkan menghasilkan insan dengan kelulusan
    terbaik serta moral yang baik pula tentunya yang siap bersaing di bursa lapangan kerja<br><br>

    Saat ini persaingan dalam bursa lapangan kerja sangat kompetitif, POLINEMA sebagai 
    perguruan tinggi terpanggil dan bertanggung jawab ikut serta berperan untuk menyiapkan 
    lulusannya yang profesional dan terbaik jika dibandingkan dengan perguruan tinggi lainnya.<br><br>

    Di era sistem teknologi informasi yang kian pesat tumbuh dan berkembang, maka perlu kiranya 
    dibangun sistem informasi terpadu, sehingga alur proses serta analisa dan evaluasi hasil 
    kelulusan dapat segera cepat diketahui dengan benar yang diperlukan sebagai alat untuk 
    pengambilan keputusan  dan informasi dalam kaitannya menuju percepatan kelulusan alumni 
    yang telah mendapatkan pekerjaan dengan waktu tunggu yang tidak terlalu lama.<br><br>

    Keterserapan lulusan  diharapkan didukung oleh sumbangsih alumni melalui wadah penelusuran 
    alumni (Tracer Study) dan jejaring atau biasanya disebut network alumni dengan cepat 
    memperoleh informasi kebutuhan lapangan pekerjaan sesuai dengan kompetensi yg dimilikinya 
    dan sesuai dengan bidang studinya.<br><br>

    Dalam kaitannya menyiapkan lulusan yang siap dalam persaingan mendapatkan pekerjaan 
