    pelatihan atau biasa disebut dengan training melalui satu program dengan wadah yang disebut 
    dengan pusat karir (Carrer Center), dengan terintegrasinya pusat karir (Carrer Center) ini 
    dengan sistem informasi secara optimal maka keterserapan lulusan dalam bursa lapangan pekerjaan 
    akan lebih siap dan dengan harapan memperoleh pekerjaan dengan masa tunggu yang tidak terlalu lama.<br><br></p>

